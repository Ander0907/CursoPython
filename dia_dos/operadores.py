x = 10
y = 17

print(f"{x} mas {y} es igual a {x+y}")
print(f"{x} menos {y} es igual a {x-y}")
print(f"{x} por {y} es igual a {x*y}")

#El resultado puede ser decimal
print(f"{x} dividido {y} es igual a {x/y}")

#El resultado será el cociente
print(f"{x} dividido al piso por {y} es igual a {x//y}")

#La operación mudulo nos ayuda adetectar que numero es par
print(f"{x} modulo de {y} es igual a {x%y}")

#Potencia
print(f"{x} elevado a la {y} es igual a {x**y}")

#Elevación al cubo
print(f"{x} elevalado a {3} es igual a {x**3}")

#Raíz cuadrada
print(f"La raíz cuadrada de {x} es {x**0.5}")